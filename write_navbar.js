"                    </div>",
"                    <div style={{ flex: 1 }}>",
"                      <div style={{ fontSize: '0.82rem', fontWeight: 600, color: '#111' }}>{p.name}</div>",
"                      <div style={{ fontSize: '0.7rem', color: '#F97316', fontWeight: 700, fontFamily: 'Syne, sans-serif' }}>{fmt(p.price)}</div>",
"                    </div>",
"                  </div>",
"                ))}",
"              </div>",
"            )}",
"          </div>",
"        </div>",
"      )}",
"    </>",
"  )",
"}"
