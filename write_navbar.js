"          {links.map(l => (",
"            <a",
"              key={l.label}",
"              href={l.href}",
"              onClick={e => { e.preventDefault(); handleNav(l.href) }}",
"              style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between', padding: '1rem clamp(1rem,4vw,3rem)', color: '#444', textDecoration: 'none', fontWeight: 600, fontSize: '0.95rem', borderBottom: '1px solid #f9f5f0' }}",
"            >",
"              {l.label}",
"              <i className='fa-solid fa-chevron-right' style={{ fontSize: '11px', color: '#F97316' }}></i>",
"            </a>",
"          ))}",
"          <div style={{ padding: '1rem clamp(1rem,4vw,3rem)' }}>",
"            <div style={{ display: 'flex', alignItems: 'center', background: '#f8f3ef', borderRadius: '14px', padding: '9px 14px', gap: '8px' }}>",
"              <i className='fa-solid fa-magnifying-glass' style={{ color: '#bbb', fontSize: '13px' }}></i>",
"              <input",
"                value={query}",
"                onChange={e => handleSearch(e.target.value)}",
"                placeholder='Rechercher un produit...'",
"                style={{ border: 'none', background: 'transparent', fontSize: '0.88rem', outline: 'none', flex: 1, fontFamily: 'Plus Jakarta Sans, sans-serif' }}",
