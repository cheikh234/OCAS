"                  <i className='fa-solid fa-face-frown-open' style={{ color: '#F97316', fontSize: '1.5rem', display: 'block', marginBottom: '6px' }}></i>",
"                  <div style={{ fontSize: '0.83rem', color: '#888' }}>",
