"            </div>",
"            {results.length > 0 && (",
"              <div style={{ marginTop: '8px', background: '#fff', borderRadius: '12px', border: '1px solid #f0e8e0', overflow: 'hidden' }}>",
"                {results.map(p => (",
"                  <div",
"                    key={p.id}",
"                    onClick={() => { handleClickResult(p); setMenuOpen(false) }}",
